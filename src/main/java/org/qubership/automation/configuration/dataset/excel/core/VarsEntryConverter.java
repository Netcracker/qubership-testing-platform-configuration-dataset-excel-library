package org.qubership.automation.configuration.dataset.excel.core;

import org.qubership.automation.configuration.dataset.excel.impl.DSCell;

import javax.annotation.Nonnull;
import javax.annotation.Nullable;

/**
 * <pre>
 * Maps some dataSetList parameter(Param) to an appropriate dataSet value(valueObj).
 * Parameter(Param) is generated by the {@link ParamsEntryConverter}
 * You can filter the values by returning the null;
 *
 * These vars will be accumulated and provided by the {@link DS#getVariables()}
 * So if you want to do mapping of Param to some Object there, use something like a
 * {@link org.apache.commons.lang3.tuple.Pair} as a returning Var here.
 *
 * </pre>
 */
public interface VarsEntryConverter<Param, Var> {

    @Nullable
    Var doVarsEntry(@Nullable DSCell entity,
                    @Nonnull DSCell param,
                    @Nonnull Param convertedParam,
                    @Nonnull DSCell value);
}
